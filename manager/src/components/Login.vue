<template>
  <el-form :model="user" :rules="rules" ref="user" label-position="left" label-width="0px" class="demo-ruleForm login-container">
    <h3 class="title">系统登录</h3>
    <el-form-item prop="account">
      <el-input type="text" v-model="user.account" auto-complete="off" placeholder="账号"></el-input>
    </el-form-item>
    <el-form-item prop="checkPass">
      <el-input type="password" v-model="user.checkPass" auto-complete="off" placeholder="密码"></el-input>
    </el-form-item>
    <el-checkbox v-model="checked" checked class="remember">记住密码</el-checkbox>
    <el-form-item style="width:100%;">
      <el-button type="primary" style="width:100%;" @click.native.prevent="login" :loading="logining">登录</el-button>
    </el-form-item>
  </el-form>
</template>

<script>
  import axios from 'axios';

  export default {
    data() {
      return {
        logining: false,
        user: {
          account: 'admin',
          checkPass: '123456'
        },
        rules: {
          account: [
            { required: true, message: '请输入账号', trigger: 'blur' },
          ],
          checkPass: [
            { required: true, message: '请输入密码', trigger: 'blur' },
          ]
        },
        checked: true
      };
    },
    methods: {
      login(){        
        axios.post('/api/user/login').then(res=>{
          console.log(res.data)
            if(res.data.success){
              sessionStorage.setItem('LoginUser',JSON.stringify(res.data.data))
              this.$router.push({ path: '/home' });
            }else{
              alert("登录失败："+res.data.msg);
            }         
        }).catch(err=>{
          alert('异常错误:'+err)
        })
      }
      
    }
  }

</script>

